async function main() {
  const conn = await mysql.createConnection(process.env.DATABASE_URL);
  console.log('✅ DB接続成功');

  // システムユーザーID=1 を使用（管理者）
  const SYSTEM_USER_ID = 1;

  let inserted = 0;
  let updated = 0;
  let skipped = 0;

  for (const tmpl of TEMPLATES) {
    // sharedId をもとに既存レコードを検索（nameとcompanyNameで照合）
    const [existing] = await conn.execute(
      'SELECT id FROM post_templates WHERE name = ? AND companyName = ?',
      [tmpl.name, tmpl.companyName]
    );

    if (existing.length > 0) {
      // 既存レコードのbusinessUnitを更新
      await conn.execute(
        'UPDATE post_templates SET businessUnit = ?, description = ?, instagramCaption = ?, instagramHashtags = ?, xCaption = ?, xHashtags = ?, threadsCaption = ?, threadsHashtags = ? WHERE id = ?',
        [
          tmpl.businessUnit,
          tmpl.description,
          tmpl.instagramCaption,
          tmpl.instagramHashtags,
          tmpl.xCaption,
          tmpl.xHashtags,
          tmpl.threadsCaption,
          tmpl.threadsHashtags,
          existing[0].id
        ]
      );
      console.log(`🔄 更新: [${tmpl.companyName}/${tmpl.businessUnit}] ${tmpl.name}`);
      updated++;
    } else {
      // 新規挿入
      await conn.execute(
        `INSERT INTO post_templates (userId, name, description, companyName, businessUnit, instagramCaption, instagramHashtags, xCaption, xHashtags, threadsCaption, threadsHashtags, isBeforeAfter, createdAt, updatedAt)
         VALUES (?, ?, ?, ?, ?, ?, ?, ?, ?, ?, ?, 0, NOW(), NOW())`,
        [
          SYSTEM_USER_ID,
          tmpl.name,
          tmpl.description,
          tmpl.companyName,
          tmpl.businessUnit,
          tmpl.instagramCaption,
          tmpl.instagramHashtags,
          tmpl.xCaption,
          tmpl.xHashtags,
          tmpl.threadsCaption,
          tmpl.threadsHashtags,
        ]
      );
      console.log(`➕ 追加: [${tmpl.companyName}/${tmpl.businessUnit}] ${tmpl.name}`);
      inserted++;
    }
  }

  // テスト用データを削除
  const [testRows] = await conn.execute(
    "SELECT id FROM post_templates WHERE name LIKE 'Performance Test%'"
  );
  if (testRows.length > 0) {
    const ids = testRows.map(r => r.id);
    await conn.execute(
      `DELETE FROM post_templates WHERE id IN (${ids.map(() => '?').join(',')})`,
      ids
    );
    console.log(`🗑️ テストデータ削除: ${testRows.length}件`);
  }

  await conn.end();
  console.log(`\n✅ 完了: 追加=${inserted}件, 更新=${updated}件, スキップ=${skipped}件`);
}

main().catch(err => {
  console.error('❌ エラー:', err);
  process.exit(1);
});
