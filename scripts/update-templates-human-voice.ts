console.log("テンプレートの更新を開始します...");

let updatedCount = 0;
let notFoundCount = 0;

for (const template of updatedTemplates) {
  // 名前でテンプレートを検索して更新
  const existing = await db
    .select()
    .from(postTemplates)
    .where(eq(postTemplates.name, template.name))
    .limit(1);

  if (existing.length === 0) {
    console.log(`  ⚠️  見つかりません: ${template.name}`);
    notFoundCount++;
    continue;
  }

  await db
    .update(postTemplates)
    .set({
      description: template.description,
      instagramCaption: template.instagramCaption,
      instagramHashtags: template.instagramHashtags,
      xCaption: template.xCaption,
      xHashtags: template.xHashtags,
      threadsCaption: template.threadsCaption,
      threadsHashtags: template.threadsHashtags,
    })
    .where(eq(postTemplates.name, template.name));

  console.log(`  ✅ 更新しました: ${template.name}`);
  updatedCount++;
}

console.log(`\n完了: ${updatedCount}件更新, ${notFoundCount}件未発見`);
await connection.end();
