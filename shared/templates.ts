/**
 * テンプレートIDからテンプレートを取得
 */
export function getTemplateById(id: string): PostTemplate | undefined {
  return POST_TEMPLATES.find(template => template.id === id);
}

/**
 * 会社名でテンプレートをフィルタリング
 */
export function getTemplatesByCompany(companyName: "ハゼモト建設" | "クリニックアーキプロ"): PostTemplate[] {
  return POST_TEMPLATES.filter(template => template.companyName === companyName);
}

/**
 * 事業区分でテンプレートをフィルタリング
 */
export function getTemplatesByBusinessUnit(businessUnit: string): PostTemplate[] {
  return POST_TEMPLATES.filter(template => template.businessUnit === businessUnit);
}

/**
 * 会社名と事業区分でグループ化して返す
 */
export function getTemplatesGroupedByBusinessUnit(companyName: "ハゼモト建設" | "クリニックアーキプロ"): Record<string, PostTemplate[]> {
  const templates = getTemplatesByCompany(companyName);
  const units = BUSINESS_UNITS.filter(u => u.companyName === companyName);
  const result: Record<string, PostTemplate[]> = {};
  for (const unit of units) {
    result[unit.id] = templates.filter(t => t.businessUnit === unit.id);
  }
  return result;
}
